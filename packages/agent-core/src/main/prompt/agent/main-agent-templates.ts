/**
 * 渲染主 Agent 模板
 *
 * 替换 [name] 占位符为实际 agent 名称。
 *
 * @param template 模板文本
 * @param name 智能体名称
 * @returns 渲染后的提示词
 */
export function renderMainAgentTemplate(template: string, name: string): string {
  return template.replace(/\[name\]/g, name);
}